        """키워드 통계 정보"""
        stats = {}
        
        for category, keyword_dict in self.keywords.items():
            category_stats = {}
            total_keywords = 0
            
            for keyword_type, keyword_list in keyword_dict.items():
                count = len(keyword_list)
                category_stats[keyword_type] = count
                total_keywords += count
            
            category_stats['total'] = total_keywords
            stats[category] = category_stats
        
        # 전체 통계
        stats['overall'] = {
            'total_categories': len(self.keywords),
            'total_keywords': sum(cat['total'] for cat in stats.values()),
            'avg_keywords_per_category': sum(cat['total'] for cat in stats.values()) / len(self.keywords)
        }
        
        return stats


